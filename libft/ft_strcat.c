char	*ft_strcat(char *s1, const char *s2)
{
	char	*copy;
	int		i;

	i = 0;
	copy = s1;
	while (*copy)
		copy++;
	while (s2[i])
	{
		*copy = s2[i];
		copy++;
		i++;
	}
	*copy = '\0';
	return (s1);
}
